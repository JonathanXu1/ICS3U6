class SavingsAccount extends BankAccount{
  double interest = 0.025;
  
  SavingsAccount(double inital){
    super.balance = initial;
  }
  SavingsAccount(double initial, double i){
    super.balance = initial;
    interest = i;
  }
  
  void addInterest(){
    super.balance = super.balance*(1 + interest);
  }
}